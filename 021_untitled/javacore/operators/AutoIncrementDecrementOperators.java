public class AutoIncrementDecrementOperators {

    public static void main(String[] args) {

        int num1 = 100;
        int num2 = 50;

        // num1 = num1 + 1
        num1++;
        // num2 = num2 - 1
        num2--;
        System.out.println("Output of num1 + 1 is " + num1);
        System.out.println("Output of num2 - 1 is " + num2);
    }
}
